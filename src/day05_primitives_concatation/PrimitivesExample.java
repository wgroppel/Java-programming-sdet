package day05_primitives_concatation;

public class PrimitivesExample {
//    Add a main method!
    public static void main(String[] args){
//        Practice using different primitives.
        // Whole number primitives.
        byte byteValue = 100;
        short shortValue = 32000;
        int intValue = 1234567890;
        // Use "L/l" after last number in "long" to
        // make sure java doesn't recognize number as "int".
        long longValue = 319548173146L;
//      Decimal numbers/Floating point types
        float floatValue = 2345.44F;
        double doubleValue = 321532.3244;
//        Character type.
        char charValue = 'g';
        char charValue2 = 'G';
//      Boolean type.
        boolean booleanValue = true;
        boolean booleanValue2 = false;
    }
}
