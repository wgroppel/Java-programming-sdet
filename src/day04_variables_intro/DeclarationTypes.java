package day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args){
//        Practice declaring a variable and assigning a value on the same line.
        int kunai = 20;
        System.out.println(kunai);
//        Practice changing value of variable.
        int shurikens = 30;
        System.out.println(shurikens);
        shurikens = 35;
        System.out.println(shurikens);
//        Practice  declaring multiple variables on the same line. Assign values to variables.
        int swords, paperBombs, scrolls;
        swords = 40;
        paperBombs = 50;
        scrolls = 60;
        System.out.println(swords);
        System.out.println(paperBombs);
        System.out.println(scrolls);
//        Practice declaring variables and assigning a value to them on the same line.
        int genin = 69, chuunin = 87, jonin = 98;
        System.out.println(genin);
        System.out.println(chuunin);
        System.out.println(jonin);
    }
}
