package day04_variables_intro;

public class VariablesPractice {
    public static void main(String[] args){
//        Declare variable students that stores a number.
        int students; // We are declaring variable ONE time.
        students = 400; // We are assigning 400.
        System.out.println(students);
        students = 420; // We are assigning 420.
        System.out.println(students);
//        Declare a new variable, teachers.
        int teachers;
        teachers = 6;
//        Practice printing using print command to make label for variables.
        System.out.print("Number of teachers:");
        System.out.println(teachers);
//        Declare a new variable, mentors,
        int mentors;
        mentors = 9;
        System.out.print("Number of mentors:");
        System.out.println(mentors);
    }
}
